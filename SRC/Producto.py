class Producto:

    def __init__(self, idProducto, precio, nombre, cantidad, marca, presentacion, refrigeracion, fechaPreparacion, fechaCaducidad):

        self.idProducto = idProducto
        self.precio = precio
        self.nombre = nombre
        self.cantidad = cantidad
        self.marca = marca
        self.presentacion = presentacion
        self.refrigeracion = refrigeracion
        self.fechaPreparacion = fechaPreparacion
        self.fechaCaducidad = fechaCaducidad
