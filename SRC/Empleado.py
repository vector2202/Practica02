class Empleado:

    def __init__(self, idEmpleado, nombre, direccion, telefono, fechaNacimiento, correo, puesto, sucursalTrabajo):

        self.idEmpleado = idEmpleado
        self.nombre = nombre
        self.direccion = direccion
        self.telefono = telefono
        self.fechaNacimiento = fechaNacimiento
        self.correo = correo
        self.puesto = puesto
        self.sucursalTrabajo = sucursalTrabajo
